# Utility function to find closest city
def find_closest_city(lat, lon):
    """Find the closest city to given coordinates."""
    import math
    min_dist = float('inf')
    closest = None
    for city, (clat, clon) in CITY_COORDS.items():
        dist = math.sqrt((lat - clat)**2 + (lon - clon)**2)
        if dist < min_dist:
            min_dist = dist
            closest = city
    return closest


def normalize_city_name(name):
    """Normalize a city name for lookup (lowercase, handle diacritics)."""
    if not name:
        return None
    name = name.lower().strip()
    if name in CITY_COORDS:
        return name
    # Try removing common suffixes
    for suffix in [' (miasto)', ' (gmina)', ' (wieś)', ' (wies)']:
        cleaned = name.replace(suffix, '')
        if cleaned in CITY_COORDS:
            return cleaned
    return name if name in CITY_COORDS else None
